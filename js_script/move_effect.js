// 技がまもる状態などに防がれると追加効果は発生しない。
// 技がみがわり状態に防がれると、追加効果は発生しない。
// 技が相性・特性により無効化されると追加効果は発生しない。
// 特性てんのめぐみまたはにじ状態で発生確率を2倍にできる。ひるみ状態にする効果に限り両者の効果は累積せず、発動率は元の2倍に留まる。それ以外のランクを変動させたり状態異常にする効果では両者の効果は累積し、元の4倍に上がる。
