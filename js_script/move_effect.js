    // どくしゅの効果はにじ状態の効果を受けない。
    // どくしゅの効果はりんぷんにより防がれる。
// 特性あくしゅうによる攻撃、または持ち物おうじゃのしるし/するどいキバによる攻撃は10％で相手をひるませる。両者の効果は重複しない。
    // 元々ひるみ効果を追加する攻撃にあくしゅう/おうじゃのしるし/するどいキバの効果は無い。(第五世代以降)
    // あくしゅう/おうじゃのしるし/するどいキバの効果はてんのめぐみ/にじ状態の効果を受ける。(第五世代以降)
    // あくしゅう/おうじゃのしるし/するどいキバの効果はりんぷんにより防がれる。

